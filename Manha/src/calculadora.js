// Função soma, precisa ter dois números e ela precisa trazer o resultado
// 2 + 3 = resultado 
const soma = function(num1, num2){
    return(num1 + num2);
}

const subtracao = function(num1, num2){
    return(num1 - num2);
}

const mult = function(num1, num2){
    return(num1 * num2);
}

const div = function(num1, num2){
    return(num1 / num2);
}
