// Soma, subtração, div, mult
// primeira função é SOMAR, precisamos de 2 numeros e um resultado
// 9 + 2
const somar = function (num01, num02) {
    return (num01 + num02);
}

const subtrair = function (num01, num02) {
    return (num01 - num02);
}

const multiplicar = function (num01, num02) {
    return (num01 * num02);
}

const dividir = function (num01, num02) {
    return (num01 / num02);
}


